package com.example.bookhair.Class;

public class ThongBao {
    private int id_salon;
    private String tenThongBao;
    private String noiDung;
    private String ngayThongBao;
    private String gioThongBao;

    public int getId_salon() {
        return id_salon;
    }

    public void setId_salon(int id_salon) {
        this.id_salon = id_salon;
    }

    public String getTenThongBao() {
        return tenThongBao;
    }

    public void setTenThongBao(String tenThongBao) {
        this.tenThongBao = tenThongBao;
    }

    public String getNoiDung() {
        return noiDung;
    }

    public void setNoiDung(String noiDung) {
        this.noiDung = noiDung;
    }

    public String getNgayThongBao() {
        return ngayThongBao;
    }

    public void setNgayThongBao(String ngayThongBao) {
        this.ngayThongBao = ngayThongBao;
    }

    public String getGioThongBao() {
        return gioThongBao;
    }

    public void setGioThongBao(String gioThongBao) {
        this.gioThongBao = gioThongBao;
    }
}